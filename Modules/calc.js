
function add(x, y) {
    return x + y;
}

function substract(x, y) {
    return x - y;
}

function multiply(x, y) {
    return x * y;
}

function divide(x, y) {
    return x / y;
}

module.exports = {
    add: add,
    substract: substract,
    multiply: multiply,
    divide: divide
};

